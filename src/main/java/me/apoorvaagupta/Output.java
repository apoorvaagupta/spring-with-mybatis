package me.apoorvaagupta;

public class Output {

    private int id;
    private int output;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getOutput() {
        return output;
    }

    public void setOutput(int output) {
        this.output = output;
    }

    public Output(int id, int output) {

        this.id = id;
        this.output = output;
    }

}
